			 <h3>Symptoms</h3>
            
            <p>Signs and symptoms of type 2 diabetes often develop slowly. In fact, you can have type 2 diabetes for years and not know it. Look for:</p>
			<ul>
			    <li><strong>Increased thirst and frequent urination.</strong> Excess sugar building up in your bloodstream causes fluid to be pulled from the tissues. This may leave you thirsty. As a result, you may drink &mdash; and urinate &mdash; more than usual.</li>
			    <li><strong>Increased hunger.</strong> Without enough insulin to move sugar into your cells, your muscles and organs become depleted of energy. This triggers intense hunger.</li>
			    <li><strong>Weight loss.</strong> Despite eating more than usual to relieve hunger, you may lose weight. Without the ability to metabolize glucose, the body uses alternative fuels stored in muscle and fat. Calories are lost as excess glucose is released in the urine.</li>
			    <li><strong>Fatigue.</strong> If your cells are deprived of sugar, you may become tired and irritable.</li>
			    <li><strong>Blurred vision.</strong> If your blood sugar is too high, fluid may be pulled from the lenses of your eyes. This may affect your ability to focus.</li>
			    <li><strong>Slow-healing sores or frequent infections.</strong> Type 2 diabetes affects your ability to heal and resist infections.</li>
			    <li><strong>Areas of darkened skin.</strong> Some people with type 2 diabetes have patches of dark, velvety skin in the folds and creases of their bodies &mdash; usually in the armpits and neck. This condition, called acanthosis nigricans, may be a sign of insulin resistance.</li>
