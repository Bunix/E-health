				<p>Your dietitian will recommend that you eat fewer animal products and refined carbohydrates, such as white bread and sweets. This healthy eating plan is recommended for everyone, even people without diabetes.</p>
				<p>You'll need to learn how to count the amount of carbohydrates in the foods you eat so that you can give yourself enough insulin to properly metabolize those carbohydrates. A registered dietitian can help you create a meal plan that fits your health goals, food preferences and lifestyle.</p>
