				    <li><strong>High blood pressure medications.</strong> Your doctor may prescribe medications known as angiotensin-converting enzyme (ACE) inhibitors or angiotensin II receptor blockers (ARBs), because these medications also can help keep your kidneys healthy. These medications are recommended for people with diabetes that have blood pressures above 140/80 millimeters of mercury (mm Hg). </li>
				    <li><strong>Aspirin.</strong> Your doctor may recommend you take baby or regular aspirin daily to protect your heart. </li>
