				<p class="lead" style="font-size: 27px;">Blood sugar monitoring</p>
				<p>Depending on what type of insulin therapy you select or require &mdash; twice daily injections, multiple daily injections or insulin pump &mdash; you may need to check and record your blood sugar level at least four times a day, and generally more often. The American Diabetes Association recommends testing blood sugar levels before meals and snacks, before bed, before exercising or driving, and if you suspect you have low blood sugar. Careful monitoring is the only way to make sure that your blood sugar level remains within your target range. Be sure to wash your hands before checking your blood sugar levels.</p>
				<p>Even if you take insulin and eat on a rigid schedule, the amount of sugar in your blood can change unpredictably. With help from your diabetes treatment team, you'll learn how your blood sugar level changes in response to food, activity, illness, medications, stress, hormonal changes and alcohol.</p>
				<p>Continuous glucose monitoring (CGM) is the newest way to monitor blood sugar levels, and may be especially helpful for preventing hypoglycemia. Plus, when used by people older than 25, the devices have been shown to lower A1C.</p>
				<p>Continuous glucose monitors attach to the body using a fine needle just under the skin that checks blood glucose level every few minutes. CGM isn't yet considered as accurate as standard blood sugar monitoring, so it's not considered a replacement method for keeping track of blood sugar, but an additional tool for some people.</p>			
