				<p>The goal is to keep your blood sugar level as close to normal as possible to delay or prevent complications. Although there are exceptions, generally, the goal is to keep your daytime blood sugar levels before meals between 70 and 130 mg/dL (3.9 to 7.2 mmol/L) and your after meal numbers no higher than 180 mg/dL (10 mmol/L) two hours after eating.</p>
				<p>Good diabetes management can be overwhelming, especially when you're first diagnosed. Take it one day at a time. And remember that you're not alone. You'll work closely with your diabetes treatment team to keep your blood sugar level as close to normal as possible.</p>

