			<p>These steps will help keep your blood sugar level closer to normal, which can delay or prevent complications.</p><div id='adsmobileBottom'></div>                            
			 <p class="lead" style="font-size: 27px;">Healthy eating</p>            
		     <p>Contrary to popular perception, there's no specific diabetes diet. However, it's important to center your diet on these high-fiber, low-fat foods:</p>
			<ul>
			    <li>Fruits</li>
			    <li>Vegetables</li>
			    <li>Whole grains</li>
			</ul>
			<p>You'll also need to eat fewer animal products, refined carbohydrates and sweets.</p>
			<p>Low glycemic index foods also may be helpful. The glycemic index is a measure of how quickly a food causes a rise in your blood sugar. Foods with a high glycemic index raise your blood sugar quickly. Low glycemic index foods may help you achieve a more stable blood sugar. Foods with a low glycemic index typically are foods that are higher in fiber.</p>
			<p>A registered dietitian can help you put together a meal plan that fits your health goals, food preferences and lifestyle. He or she can also teach you how to monitor your carbohydrate intake and let you know about how many carbohydrates you need to eat with your meals and snacks to keep your blood sugar levels more stable.</p>
			            
			         
			                       
			            <p class="lead" style="font-size: 27px;">Physical activity</p>
			            
			            <p>Everyone needs regular aerobic exercise, and people who have type 2 diabetes are no exception. Get your doctor's OK before you start an exercise program. Then choose activities you enjoy, such as walking, swimming and biking. What's most important is making physical activity part of your daily routine.</p>
			<p>Aim for at least 30 minutes of aerobic exercise five days of the week. Stretching and strength training exercises are important, too. If you haven't been active for a while, start slowly and build up gradually.</p>
			<p>A combination of exercises &mdash; aerobic exercises, such as walking or dancing on most days, combined with resistance training, such as weightlifting or yoga twice a week &mdash; often helps control blood sugar more effectively than either type of exercise alone.</p>
			<p>Remember that physical activity lowers blood sugar. Check your blood sugar level before any activity. You might need to eat a snack before exercising to help prevent low blood sugar if you take diabetes medications that lower your blood sugar.</p>
			            
			         
			                       
			            <p class="lead" style="font-size: 27px;">Monitoring your blood sugar</p>
			            
			            <p>Depending on your treatment plan, you may need to check and record your blood sugar level every now and then or, if you're on insulin, multiple times a day. Ask your doctor how often he or she wants you to check your blood sugar. Careful monitoring is the only way to make sure that your blood sugar level remains within your target range.</p>
			<p>Sometimes, blood sugar levels can be unpredictable. With help from your diabetes treatment team, you'll learn how your blood sugar level changes in response to food, exercise, alcohol, illness and medication.</p>
			            
			         
			                       
			             <p class="lead" style="font-size: 27px;">Diabetes medications and insulin therapy</p>
			            
			            <p>Some people who have type 2 diabetes can achieve their target blood sugar levels with diet and exercise alone, but many also need diabetes medications or insulin therapy. The decision about which medications are best depends on many factors, including your blood sugar level and any other health problems you have. Your doctor might even combine drugs from different classes to help you control your blood sugar in several different ways.</p>
			<p>Examples of possible treatments for type 2 diabetes include:</p>
			<ul>
			    <li>
			    <p><strong>Metformin (Glucophage, Glumetza, others).</strong> Generally, metformin is the first medication prescribed for type 2 diabetes. It works by improving the sensitivity of your body tissues to insulin so that your body uses insulin more effectively.</p>
			    <p>Metformin also lowers glucose production in the liver. Metformin may not lower blood sugar enough on its own. Your doctor will also recommend lifestyle changes, such as losing weight and becoming more active.</p>
			    <p>Nausea and diarrhea are possible side effects of metformin. These side effects usually go away as your body gets used to the medicine. If metformin and lifestyles changes aren't enough to control your blood sugar level, other oral or injected medications can be added.</p>
			    </li>
			    <li><strong>Sulfonylureas.</strong> These medications help your body secrete more insulin. Examples of medications in this class include glyburide (DiaBeta, Glynase), glipizide (Glucotrol) and glimepiride (Amaryl). Possible side effects include low blood sugar and weight gain.</li>
			    <li>
			    <p><strong>Meglitinides.</strong> These medications work like sulfonylureas by stimulating the pancreas to secrete more insulin, but they're faster acting, and the duration of their effect in the body is shorter. They also have a risk of causing low blood sugar, but this risk is lower than with sulfonylureas.</p>
			    <p>Weight gain is a possibility with this class of medications as well. Examples include repaglinide (Prandin) and nateglinide (Starlix).</p>
			    </li>
			    <li>
			    <p><strong>Thiazolidinediones.</strong> Like metformin, these medications make the body's tissues more sensitive to insulin. This class of medications has been linked to weight gain and other more-serious side effects, such as an increased risk of heart failure and fractures. Because of these risks, these medications generally aren't a first-choice treatment.</p>
			    <p>Rosiglitazone (Avandia) and pioglitazone (Actos) are examples of thiazolidinediones.</p>
			    </li>
			    <li><strong>DPP-4 inhibitors.</strong> These medications help reduce blood sugar levels, but tend to have a modest effect. They don't cause weight gain. Examples of these medications are sitagliptin (Januvia), saxagliptin (Onglyza) and linagliptin (Tradjenta).</li>
			    <li>
			    <p><strong>GLP-1 receptor agonists.</strong> These medications slow digestion and help lower blood sugar levels, though not as much as sulfonylureas. Their use is often associated with some weight loss. This class of medications isn't recommended for use by itself.</p>
			    <p>Exenatide (Byetta) and liraglutide (Victoza) are examples of GLP-1 receptor agonists. Possible side effects include nausea and an increased risk of pancreatitis.</p>
			    </li>
			    <li>
			    <p><strong>SGLT2 inhibitors.</strong> These are the newest diabetes drugs on the market. They work by preventing the kidneys from reabsorbing sugar into the blood. Instead, the sugar is excreted in the urine.</p>
			    <p>Examples include canagliflozin (Invokana) and dapagliflozin (Farxiga). Side effects may include yeast infections and urinary tract infections, increased urination and hypotension.</p>
			    </li>
			    <li>
			    <p><strong>Insulin therapy.</strong> Some people who have type 2 diabetes need insulin therapy as well. In the past, insulin therapy was used as a last resort, but today it's often prescribed sooner because of its benefits.</p>
			    <p>Because normal digestion interferes with insulin taken by mouth, insulin must be injected. Depending on your needs, your doctor may prescribe a mixture of insulin types to use throughout the day and night. Often, people with type 2 diabetes start insulin use with one long-acting shot at night.</p>
			    <p>Insulin injections involve using a fine needle and syringe or an insulin pen injector &mdash; a device that looks similar to an ink pen, except the cartridge is filled with insulin.</p>
			    <p>There are many types of insulin, and they each work in a different way. Options include:</p>
			    </li>
			    <ul>
			        <li>Insulin glulisine (Apidra)</li>
			        <li>Insulin lispro (Humalog)</li>
			        <li>Insulin aspart (Novolog)</li>
			        <li>Insulin glargine (Lantus) </li>
			        <li>Insulin detemir (Levemir)</li>
			        <li>Insulin isophane (Humulin N, Novolin N)</li>
			    </ul>
			</ul>
			<p>Discuss the pros and cons of different drugs with your doctor. Together you can decide which medication is best for you after considering many factors, including costs and other aspects of your health.</p>
			<p>In addition to diabetes medications, your doctor might prescribe low-dose aspirin therapy as well as blood pressure and cholesterol-lowering medications to help prevent heart and blood vessel disease.</p>		            
