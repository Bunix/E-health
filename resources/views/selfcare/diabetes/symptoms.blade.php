			<p class="lead" style="font-size: 27px;"> When to see a doctor</p>
			<p>Consult your doctor if you notice any of the above signs and symptoms in you or your child.</p>
			<p class="lead" style="font-size: 27px;"> Signs of trouble</p>
			<p>Despite your best efforts, sometimes problems will arise. Certain short-term complications of type 1 diabetes, such as hypoglycemia, require immediate care.</p>
			<p><strong>Low blood sugar (hypoglycemia).</strong> This occurs when your blood sugar level drops below your target range. Ask your doctor what's considered a low blood sugar level for you. Blood sugar levels can drop for many reasons, including skipping a meal, getting more physical activity than normal or injecting too much insulin.</p>
			<p>Learn the symptoms of low blood sugar, and test your blood sugar if you think your blood sugar levels are dropping. When in doubt, always do a blood sugar test. Early signs and symptoms of low blood sugar include:</p>
			<ul>
			    <li>Sweating </li>
			    <li>Shakiness </li>
			    <li>Hunger </li>
			    <li>Dizziness or lightheadedness </li>
			    <li>Rapid or irregular heart rate </li>
			    <li>Fatigue </li>
			    <li>Headaches </li>
			    <li>Blurred vision </li>
			    <li>Irritability </li>
			</ul>
			<p>Later signs and symptoms of low blood sugar, which can sometimes be mistaken for alcohol intoxication in teens and adults include:</p>
			<ul>
			    <li>Lethargy </li>
			    <li>Confusion </li>
			    <li>Behavior changes, sometimes dramatic </li>
			    <li>Poor coordination </li>
			    <li>Convulsions </li>
			</ul>
			<p>If you develop hypoglycemia during the night, you might wake with sweat-soaked pajamas or a headache. Due to a natural rebound effect, nighttime hypoglycemia might cause an unusually high blood sugar reading first thing in the morning.</p>
			<p>If you have a low blood sugar reading:</p>
			<ul>
			    <li>Have 15 to 20 grams of a fast-acting carbohydrate, such as fruit juice, glucose tablets, hard candy, regular (not diet) soda or another source of sugar. Foods with added fat, such as chocolate or ice cream, don't raise blood sugar as quickly because fat slows down the absorption of the sugar. </li>
			    <li>Retest your blood sugar in about 15 minutes to make sure it's normal. </li>
			    <li>If it's still low, have another 15 to 20 grams of carbohydrate from juice, candy, glucose tablets or other source of sugar, and retest in another 15 minutes. </li>
			    <li>Repeat until you get a normal reading. </li>
			    <li>Eat a mixed food source, such as peanut butter and crackers, to help stabilize your blood sugar. </li>
			</ul>
			<p>If a blood glucose meter isn't readily available, treat for low blood sugar anyway if you have symptoms of hypoglycemia, and then test as soon as possible.</p>
			<p>Always carry a source of fast-acting sugar with you. Left untreated, low blood sugar will cause you to lose consciousness. If this occurs, you may need an emergency injection of glucagon &mdash; a hormone that stimulates the release of sugar into the blood. Be sure you always have a glucagon emergency kit available &mdash; at home, at work, when you're out &mdash; and make sure it hasn't expired.</p>
			<p><strong>Hypoglycemia unawareness.</strong> Some people may lose the ability to sense that their blood sugar levels are getting low, because they've developed a condition known as hypoglycemia unawareness. With hypoglycemia unawareness, the body no longer reacts to a low blood sugar level with symptoms such as lightheadedness or headaches. The more you experience low blood sugars, the more likely you are to develop hypoglycemia unawareness. The good news is that if you can avoid having a hypoglycemic episode for several weeks, you may start to become more aware of impending lows.</p>
			<p><strong>High blood sugar (hyperglycemia).</strong> Your blood sugar can rise for many reasons, including eating too much, eating the wrong types of foods, not taking enough insulin or illness.</p>
			<p>Watch for:</p>
			<ul>
			    <li>Frequent urination </li>
			    <li>Increased thirst </li>
			    <li>Blurred vision </li>
			    <li>Fatigue </li>
			    <li>Irritability </li>
			    <li>Hunger </li>
			    <li>Difficulty concentrating </li>
			</ul>
			<p>If you suspect hyperglycemia, check your blood sugar. You might need to adjust your meal plan or medications. If your blood sugar is higher than your target range, you'll likely need to administer a "correction" using an insulin shot or through an insulin pump. A correction is an additional dose of insulin that should bring your blood sugar back into the normal range. High blood sugar levels don't come down as quickly as they go up. Ask your doctor how long to wait until you recheck. If you use an insulin pump, random high blood sugar readings may mean you need to change the pump site.</p>
			<p>If you have a blood sugar reading above 240 mg/dL (13.3 mmol/L), test for ketones using a urine test stick. Don't exercise if your blood sugar level is above 240 mg/dL or if ketones are present. If only a trace or small amounts of ketones are present, drink extra fluids to flush out the ketones.</p>
			<p>If your blood sugar is persistently above 300 mg/dL (16.7 mmol/L), despite taking appropriate correction doses of insulin, call your doctor or seek emergency care.</p>
			<p><strong>Increased ketones in your urine (diabetic ketoacidosis).</strong> If your cells are starved for energy, your body may begin to break down fat &mdash; producing toxic acids known as ketones. Diabetic ketoacidosis is a life-threatening emergency.</p>
			<p>Signs and symptoms of this serious condition include:</p>
			<ul>
			    <li>Nausea </li>
			    <li>Vomiting </li>
			    <li>Abdominal pain </li>
			    <li>A sweet, fruity smell on your breath </li>
			    <li>Weight loss </li>
			</ul>
			<p>If you suspect ketoacidosis, check your urine for excess ketones with an over-the-counter ketones test kit. If you have large amounts of ketones in your urine, call your doctor right away or seek emergency care. Also, call your doctor if you have vomited more than once and you have ketones in your urine.</p>
			
